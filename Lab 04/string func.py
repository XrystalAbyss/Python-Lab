text = "Hello, Wellcome To My Python programming world!"

lower_case = text.lower()
print("Lowercase:", lower_case)

upper_case = text.upper()
print("Uppercase:", upper_case)

capitalized = text.capitalize()
print("Capitalized:", capitalized)

count_python = text.count("Python")
print("Occurrences of 'Python':", count_python)

replaced_text = text.replace("Python", "Java")
print("Replaced 'Python' with 'Java':", replaced_text)

split_text = text.split(" ")
print("Split text:", split_text)

text_with_spaces = "   Hello, Python!   "
trimmed_text = text_with_spaces.strip()
print("Trimmed text:", trimmed_text)
